# Даны кортеж из индексов и кортеж из значений. Создайте кортеж из значений
# в порядке, указанном в кортеже из индексов

indexes_tuple = (1, 2, 0, 2, 0, 1)
values_tuple = ('Пример-1', 'Пример-2', 'Пример-3')

# Пример решения с помощью цикла for
result = []
for idx in indexes_tuple:
    result.append(values_tuple[idx])
result = tuple(result)
print(result)
# Результат:
# ('Пример-2', 'Пример-3', 'Пример-1', 'Пример-3', 'Пример-1', 'Пример-2')

